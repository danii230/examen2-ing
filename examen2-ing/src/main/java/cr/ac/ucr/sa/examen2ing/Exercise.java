package cr.ac.ucr.sa.examen2ing;

public class Exercise {
	 private int id;

	    private String name;

	    public Exercise(int id, String name) {
	        this.id = id;
	        this.name = name;
	    }

	    public int getId() {
	        return id;
	    }

	    public String getName() {
	        return name;
	    }
}
